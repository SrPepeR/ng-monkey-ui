/**
 * Represents the different screen sizes.
 */
export enum ScreenSize {

  /**
   * Extra small screen size (600px).
   */
  XS = 600,

  /**
   * Small screen size (768px).
   */
  SM = 768,

  /**
   * Medium screen size (992px).
   */
  MD = 992,

  /**
   * Large screen size (1200px).
   */
  LG = 1200,

  /**
   * Extra large screen size (1400px).
   */
  XL = 1400,

  /**
   * Double extra large screen size (1600px).
   */
  XXL = 1600

}

/**
 * Represents the different screen size style classes.
 */
export enum ScreenSizeStyleClass {

  XS = 'screen-xs',
  SM = 'screen-sm',
  MD = 'screen-md',
  LG = 'screen-lg',
  XL = 'screen-xl',
  XXL = 'screen-xxl'

}

/**
 * Represents the possible screen orientations.
 */
export enum ScreenOrientation {

  PORTRAIT = 'portrait',
  LANDSCAPE = 'landscape',

}
